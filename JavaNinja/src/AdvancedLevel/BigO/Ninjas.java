package AdvancedLevel.BigO;

public class Ninjas {

    String name;
    int age;

    public Ninjas(String name, int age) {
        this.name = name;
        this.age = age;
    }

    @Override
    public String toString() {
        return "Ninjas{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
